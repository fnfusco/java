package entities;

public class Matriz {
	
	public Matriz() {
	}
	
	public static void desenhaMatriz() {
		int[][] matriz = new int[8][8];
		int l=0,c=0;
		
		for(l=0;l<matriz.length;l++) {
			for(c=0; c<matriz.length; c++) {
				if (c == 0)
					matriz[l][c] = 1;
				else if ( c==l )
					matriz[l][c] = 1;
				else	
				matriz[l][c] = 0;
				System.out.print(matriz[l][c] + " ");
			}		
			System.out.println();
		}
				
	}
	
}


